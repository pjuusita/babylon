class PDFTableDateColumn extends PDFComponent {

	public $column_name = null;
	public $column_width = null;
	public $column_height = null;
	public $primary_column = true;
	public $dateFormat	= 'Y-m-d';
	public $type		= 'date';
	
	//********************************************************************************************************************************************************************
	//**
	//**
	//********************************************************************************************************************************************************************
	
	public function __construct($column_name,$column_datavariable,$column_width,$dateFormat) {

		$this->column_name 			= $column_name;
		$this->column_datavariable 	= $column_datavariable;
		$this->column_width 		= $column_width;
		$this->dateFormat			= $dateFormat;

	}
	
	//********************************************************************************************************************************************************************
	//**
	//**
	//********************************************************************************************************************************************************************
	
	public function setWidth($width) {
		
		$this->column_width = $width;
		
	}
	
	//********************************************************************************************************************************************************************
	//**
	//**
	//********************************************************************************************************************************************************************
	
	public function setDatavariable($datavariable) {
		
		$this->column_datavariables = $datavariable;
		
	}
	
	//********************************************************************************************************************************************************************
	//**
	//**
	//********************************************************************************************************************************************************************
	
	public function setName($name) {
		
		$this->column_name = $name;
		
	}
	
	//********************************************************************************************************************************************************************
	//**
	//**
	//********************************************************************************************************************************************************************
	
	public function format($value) {
	
		$dateComponents = explode("-",$value);
		$dateFormat		= $this->dateFormat;
		$time			= mktime(0,0,0,$dateComponents[1],$dateComponents[2],$dateComponents[0]);
		$date 			= date($dateFormat,$time);
		
		return $date;
	
	}
	
	//********************************************************************************************************************************************************************
	//**
	//**
	//********************************************************************************************************************************************************************
	
	public function show() {
		
	}
}
