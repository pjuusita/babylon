<?php

abstract class DOCElement {
	
	protected $x;
	protected $y;
	
	protected $width;
	protected $height;
	
	protected $data;
	
	protected $headerfont = null;
	protected $contentfont = null;
	protected $drawcolor   = null;
	
	abstract public function draw($DOC);
	
//************************************************************************************************************************************
//*** PUBLIC FUNCTION SETDATA($DATA)
//*** Sets data used by docelement.
//************************************************************************************************************************************
	
	public function setData($data) {
		$this->data = $data;
	}
	
//************************************************************************************************************************************
//*** PUBLIC FUNCTION SETX($X)
//*** Cursor manipulation x.
//************************************************************************************************************************************

	public function setX($x) {
		$this->x = $x;
	}

//************************************************************************************************************************************
//*** PUBLIC FUNCTION SETY($Y)
//*** Cursor manipulation y.
//************************************************************************************************************************************
		
	public function setY($y) {
		$this->y = $y;
	}

//************************************************************************************************************************************
//*** PUBLIC FUNCTION MOVEXY($X,$Y)
//*** Cursor manipulation move by x,y.
//************************************************************************************************************************************	
	
	public function moveXY($x,$y) {
		
		if (get_class($this)=="DOCLine") {

			$this->moveXY2($x,$y);

		} else {
		
			$this->x = $this->x + $x;
			$this->y = $this->y + $y;
		}
	}
	
//************************************************************************************************************************************
//*** PUBLIC FUNCTION SETHEADERFONT($FONT)
//*** Sets header-font.
//************************************************************************************************************************************
	
	public function setHeaderFont($font) {
	
		$this->headerfont = $font;
	
	}

//************************************************************************************************************************************
//*** PUBLIC FUNCTION SETCONTENTFONT($FONT)
//*** Sets content-font.
//************************************************************************************************************************************
		
	public function setContentFont($font) {
	
		$this->contentfont = $font;
	
	}
	
//************************************************************************************************************************************
//*** PUBLIC FUNCTION SETDRAWCOLOR($COLOR)
//*** Sets draw-color.
//************************************************************************************************************************************
	
	public function setDrawColor($color) {
		
		$this->drawcolor = $color;
	}
	
//************************************************************************************************************************************
//*** PUBLIC FUNCTION GETHEADERFONT()
//*** Gets header-font.
//************************************************************************************************************************************
		
	public function getHeaderFont() {
	
		return $this->headerfont;
	
	}

//************************************************************************************************************************************
//*** PUBLIC FUNCTION GETCONTENTFONT()
//*** Gets content-font.
//************************************************************************************************************************************
	
	public function getContentFont() {
	
		return $this->contentfont;
	
	}
	
//************************************************************************************************************************************
//*** PUBLIC FUNCTION GETX()
//*** Returns x.
//************************************************************************************************************************************

	public function getX() {
		
		return $this->x;
	}
	
//************************************************************************************************************************************
//*** PUBLIC FUNCTION GETY()
//*** Returns y.
//************************************************************************************************************************************
	
	public function getY() {
	
		return $this->y;
	}
	
//************************************************************************************************************************************
//*** PUBLIC FUNCTION GETWIDTH()
//*** Returns width.
//************************************************************************************************************************************
	
	public function getWidth() {
	
		return $this->width;
	}
	
//************************************************************************************************************************************
//*** PUBLIC FUNCTION GETHEIGHT()
//*** Returns height.
//************************************************************************************************************************************
	
	public function getHeight() {
	
		return $this->height;
		
	}
}

?>